const escapeTemplateLiteral = (value) =>
  value.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");

const findLatestMarkdown = (dirPath) => {
  if (!fs.existsSync(dirPath)) return null;
  const files = fs
    .readdirSync(dirPath)
    .filter((name) => name.toLowerCase().endsWith(".md"))
    .sort((a, b) => b.localeCompare(a, "en"));
  return files[0] ?? null;
};

const lines = [];
lines.push("// Auto-generated by scripts/generate-legal-content.mjs");
lines.push("// Do not edit this file directly.");
lines.push("");

for (const source of sources) {
  const targetDir = path.join(docsRoot, source.folder, source.locale);
  const latestFile = findLatestMarkdown(targetDir);
  const content = latestFile
    ? fs.readFileSync(path.join(targetDir, latestFile), "utf8")
    : `${source.emptyTitle}${source.emptyBody}`;
  const escaped = escapeTemplateLiteral(content);
  const sourcePath = `legal/${source.folder}/${source.locale}/${latestFile ?? "(missing)"}`;
  lines.push(`// source: ${sourcePath}`);
  lines.push(`export const ${source.key} = \`${escaped}\`;`);
  lines.push("");
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, `${lines.join("\n")}\n`, "utf8");
console.log(`[legal] generated: ${path.relative(projectRoot, outputPath)}`);
